"""
O Python possui uma série de módulos embutidos que fornecem funcionalidades essenciais para uma ampla gama de tarefas de
 programação. Aqui está uma lista de alguns dos módulos built-in mais comuns e suas principais funções:

1. **`os`**: Fornece uma maneira de interagir com o sistema operacional, permitindo operações como manipulação de
arquivos e diretórios.

   - Exemplo: `os.listdir()` para listar arquivos em um diretório.

2. **`sys`**: Fornece acesso a variáveis e funções que interagem diretamente com o interpretador Python.

   - Exemplo: `sys.argv` para acessar os argumentos de linha de comando passados para um script.

3. **`math`**: Fornece funções matemáticas básicas, como operações aritméticas, exponenciação e funções trigonométricas.

   - Exemplo: `math.sqrt()` para calcular a raiz quadrada.

4. **`datetime`**: Fornece classes para manipulação de datas e horas.

   - Exemplo: `datetime.datetime.now()` para obter a data e hora atuais.

5. **`random`**: Contém funções para gerar números aleatórios e realizar seleções aleatórias.

   - Exemplo: `random.randint()` para gerar um número inteiro aleatório dentro de um intervalo.

6. **`re`**: Oferece suporte para expressões regulares, permitindo realizar buscas e manipulações avançadas de strings.

   - Exemplo: `re.search()` para encontrar padrões em uma string.

7. **`json`**: Fornece funções para trabalhar com dados no formato JSON (JavaScript Object Notation).

   - Exemplo: `json.loads()` para converter uma string JSON em um objeto Python.

8. **`urllib`**: Fornece funções para trabalhar com URLs e fazer solicitações HTTP.

   - Exemplo: `urllib.request.urlopen()` para abrir uma URL.

9. **`itertools`**: Contém funções que criam iteradores eficientes para loops e combinações.

   - Exemplo: `itertools.product()` para gerar o produto cartesiano de iteráveis.

10. **`functools`**: Oferece ferramentas para operações de função de alta ordem, como funções de memória e decoradores.

    - Exemplo: `functools.lru_cache()` para armazenar em cache resultados de funções.

11. **`collections`**: Contém tipos de dados de containers especializados, como listas, dicionários e conjuntos com
funcionalidades adicionais.

    - Exemplo: `collections.Counter()` para contar ocorrências de itens em um iterável.

12. **`csv`**: Facilita a leitura e escrita de arquivos no formato CSV (Comma-Separated Values).

    - Exemplo: `csv.reader()` para ler dados de um arquivo CSV.

13. **`sqlite3`**: Fornece uma interface para trabalhar com bancos de dados SQLite.

    - Exemplo: `sqlite3.connect()` para conectar a um banco de dados SQLite.

14. **`socket`**: Fornece uma interface para a comunicação de rede via sockets.

    - Exemplo: `socket.socket()` para criar um novo objeto de socket.

15. **`logging`**: Oferece um sistema de registro de logs que pode ser configurado para diferentes níveis de severidade
e destinos.

    - Exemplo: `logging.info()` para registrar uma mensagem de informação.

Cada um desses módulos tem uma documentação completa que descreve suas funções e como usá-las. A documentação oficial do
Python é um ótimo recurso para explorar mais sobre esses módulos e suas capacidades.


"""